package com.example.test3.repostory;

public class FurnitureModel {

    private String name;
    private double price;
    private double length;
    private double width;
    private double height;
    private String color;
    private String picture;



    public FurnitureModel(String name, double price, double length, double width, double height, String color, String picture) {
        this.name = name;
        this.price = price;
        this.length = length;
        this.width = width;
        this.height = height;
        this.color = color;
        this.picture = picture;
    }
}
